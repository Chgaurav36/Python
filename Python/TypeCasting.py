######  Type Casting is a process of converting a variable from one data type to another
#         str(),     float(),   int(),      bool()

name = "Gaurav Chaurasia"
age = 25
gpa = 3.2
is_student = True

print(type(name))
print(type(age))
print(type(gpa))
print(type(is_student))

#Result
#   <class 'str'>
#   <class 'int'>
#   <class 'float'>
#   <class 'bool'>