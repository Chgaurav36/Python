#########################   STRING  SLICING     ############################

# We can return a range of characters by using slice syntax
# By providing a start index and end index seperated with colon

a = "This is a String"

print(a[:])
print(a[2:8])  #This will print after 2nd index till 7th index
print(a[4:])   #This will print after 4th index i.e. from 5th index
print(a[:9])  #This will print till 8th index    
